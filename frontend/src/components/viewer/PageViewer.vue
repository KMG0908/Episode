"저는 스승님의 아들 같은 존재입니다."

이 더러운 스승에게서 살아남기 위하여 아부 정도는 패시브 스킬이다. 그는 20년간 이렇게 버텼다.

"이 아들은 스승님의 비호 없이 험난한 세상을 살아갈 자신이 없습니다."

"...그렇지 않다. 나보단 많이 약하지만 그래도 많이 강해졌다."

