올림푸스에서는 석유도 구할 수 있다. 더 정확히 말하자면 석유의 역할을 대신하는 '몬스터 스톤'이지만, 어쨌거나 그걸 구해서 현실로 전송할 수 있다. 올림푸스의 물건을 현실로. 인류는 이를 통해 발전해왔고 이걸 올림푸스 혁명이라 부른다.

올림푸스는 현실을 유지하는 하나의 수단이었으며 올림푸스를 지배하는 자가 곧 현실을 지배하는 자가 되었다. 올림푸스의 산물이 이 현대사회를 이끌어갔으니까.

