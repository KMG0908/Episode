6살 때. 한주혁은 어느 미친놈에게 납치를 당했다. 그 미친놈이 이렇게 말했었다.

- 드디어 재능을 가진 아이를 찾았구나. 이런 재능을 가진 아이를 찾다니. 클클클! 천하의 무골이로고! 내 너를 세상에서 가장 강한 사내로 만들어 주겠다!

그렇게 20년이 흘러버렸다.

"이제 나는 죽어도 여한이 없구나. 내 모든 것을 전수했으니."

제발 어서 죽어주세요. 한주혁은 이 미친 작자에게 일말의 동정심도 품지 않았다. 정(情)도 느껴지지 않았다. 지금 집안에서 밥버러지, 만년 취준생 취급을 받고 있는 게 바로 이 미친 스승놈때문 아니겠는가.

20년간 납치 및 감금, 폭행, 협박, 괴롭힘을 받으며 이 늙은이를 위해 무료봉사를 해왔다. 자신을 납치하여 20년간 괴롭힌 당사자에게 무슨 미련이 남으랴. 몇 번이나 탈출을 시도했다가 정말 죽기 일보직전까지 얻어맞았다. 살아있는 상태로 하이에나들에게 뜯겨먹기도 했다. 그 때만 떠올리면 아직도 소름이 돋는다. 그 기억들이 아직도 생생했다.

'그래도 나는 결국 버텼어.'

어떻게 버텼는지. 자신이 왜 버텼는지. 그것도 잘 모르겠다. 일반적인 경우라면, 캐릭터 삭제를 하고 다시 키웠을 거다. 그게 당연한 거고 상식이다. 이런 경우를 일컬어 똥통에 빠졌다고 표현하니까. 그런데 한주혁은 그 똥통에서 어찌어찌 살아남았다.

"스승님."

