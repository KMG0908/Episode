그래서 사람들은 편의상 '잃어버린 문명'을 제외하고서 지금을 DC 2016년이라고 표기하고 있다.

'잃어버린 문명' 자체를 부정하는 사람은 없었다. 증거가 뚜렷하게 남아있었으니까. 그 증거는 바로 시스템 '제우스'였다. 이 제우스를 누가 어디서 어떻게 만들었는지는 밝혀지지 않았다. 다만 이 '제우스'는 서울 여의도에 위치한 커다란 돔 안에 있다고 알려져 있다. 과거, 한국의 국회의사당이 있던 그 자리다. 그 자리는 커다란 돔에 둘러싸여 있으며 그 누구의 발길도 허락하지 않는다고 알려져 있다.

저 돔이 어떤 물질로 구성되어 있는지, 어떻게 하면 저 돔 안으로 들어갈 수 있는지도 모른다. 내부가 어떻게 생겼는지도 알 수 없다. 다만, 제우스는 실재하고 있는 '잃어버린 문명의 잔재'이며 그 잔재는 기록이 다시 시작된 지난 200년 동안, 가상현실게임 올림푸스를 운영하고 있다.

