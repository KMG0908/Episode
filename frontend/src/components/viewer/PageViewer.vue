어차피 가르침을 줄 수 없다. 이제 이 납치, 폭행, 감금을 자행한 범죄자는 죽는다. 그건 누구보다도 한주혁 자신이 알고 있다. 그가 원하는 건 혹시 모를 추가 퀘스트나 보상.

"내 너의 마음을 잘 알았다. 잊지 말거라. 너는 나의 하나뿐인 제자이며, 나의 진전을 이은 유일한 전승자라는 것을."

네. 그 빌어먹을 전승자라는 이유 때문에 20년간 감금을 당했지요. 납치범이 지도를 건넸다.

